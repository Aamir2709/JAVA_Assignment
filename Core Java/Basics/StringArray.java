public class StringArray {
    public static void main(String[] args) {
        // String example
        String greeting = "Hello, World!";
        System.out.println("String Example: " + greeting);

        // Array example
        int[] numbers = {1, 2, 3, 4, 5};
        System.out.print("Array Example: ");
        for (int num : numbers) {
            System.out.print(num + " ");
        }
    }
}
